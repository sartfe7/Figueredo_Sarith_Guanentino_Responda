    <h1 class="page-title">📘 Examen Saber 11.º</h1>

    <div class="text-box">
      <p>
        El <strong>Examen Saber 11.º</strong> es una evaluación estandarizada fundamental en Colombia, diseñada y publicada por el <strong>Instituto Colombiano para la Evaluación de la Educación (Icfes)</strong>. 
        La guía de orientación que se ha proporcionado corresponde a la edición <strong>2025-1, Calendario B</strong>.
      </p>

      <h2>🎯 Objetivos del Examen Saber 11.º</h2>
      <p>Los objetivos de esta prueba, de acuerdo con el Decreto 869 de 2010, son multifacéticos y buscan beneficiar a estudiantes, instituciones y el sistema educativo en general:</p>
      <ul>
        <li><strong>Comprobar el desarrollo de competencias:</strong> Verificar el grado de desarrollo de las competencias de los estudiantes que están finalizando el grado undécimo.</li>
        <li><strong>Autoevaluación y proyecto de vida:</strong> Proporcionar a los estudiantes elementos para su autoevaluación y el desarrollo de su proyecto de vida.</li>
        <li><strong>Información para la educación superior:</strong> Ofrecer a las universidades información relevante sobre los aspirantes y admitidos.</li>
        <li><strong>Monitorear la calidad educativa:</strong> Supervisar la calidad de la educación en los colegios del país.</li>
        <li><strong>Indicadores de valor agregado:</strong> Generar información para evaluar educación media y superior.</li>
        <li><strong>Inspección y vigilancia:</strong> Servir de fuente para la construcción de indicadores de calidad educativa.</li>
        <li><strong>Autoevaluación institucional:</strong> Proporcionar información para mejorar las prácticas pedagógicas de los colegios.</li>
        <li><strong>Políticas educativas:</strong> Apoyar la creación de políticas educativas nacionales e institucionales.</li>
      </ul>

      <h2>👥 ¿A Quiénes Evalúa?</h2>
      <p>Principalmente a los <strong>estudiantes de grado 11.º</strong>. También pueden presentarlo personas <strong>mayores de 18 años</strong> para obtener su título de bachiller.</p>

      <h2>📑 Estructura del Examen</h2>
      <p>El examen Saber 11.º se compone de <strong>cinco pruebas y un cuestionario socioeconómico</strong>:</p>
      <ul>
        <li><strong>Matemáticas</strong></li>
        <li><strong>Lectura Crítica</strong></li>
        <li><strong>Sociales y Ciudadanas</strong></li>
        <li><strong>Ciencias Naturales</strong></li>
        <li><strong>Inglés</strong></li>
        <li><strong>Cuestionario socioeconómico</strong> (confidencial, no afecta el puntaje)</li>
      </ul>
      <p>Se divide en <strong>dos sesiones</strong>, cada una de <strong>4h 30min</strong>.</p>

      <h2>📘 Tipos de Cuadernillos</h2>
      <ol>
        <li><strong>Estándar:</strong> Para la mayoría de aspirantes.</li>
        <li><strong>Para comunidades indígenas o étnicas:</strong> Versión sin Inglés.</li>
        <li><strong>Ajustes para discapacidad:</strong> versiones especiales (auditiva, visual, cognitiva, etc.)</li>
      </ol>

      <h2>❓ Tipos de Pregunta</h2>
      <p>Son <strong>selección múltiple con única respuesta</strong>. Cada una incluye un enunciado, la tarea y opciones.</p>

      <h2>📝 Materiales del Examen</h2>
      <ul>
        <li><strong>Cuadernillo</strong></li>
        <li><strong>Hoja de respuestas</strong></li>
        <li><strong>Hoja de operaciones</strong> (no calificada)</li>
      </ul>

      <h2>📊 Competencias Evaluadas</h2>
      <ul>
        <li><strong>Matemáticas:</strong> interpretación, formulación, argumentación.</li>
        <li><strong>Lectura Crítica:</strong> identificar contenidos, comprender sentido global, reflexionar.</li>
        <li><strong>Sociales y Ciudadanas:</strong> pensamiento social, análisis de perspectivas, pensamiento crítico.</li>
        <li><strong>Ciencias Naturales:</strong> explicación de fenómenos, uso del conocimiento, indagación.</li>
        <li><strong>Inglés:</strong> comunicación efectiva según el Marco Común Europeo.</li>
      </ul>

