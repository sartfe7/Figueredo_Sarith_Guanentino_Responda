    <h1 class="page-title">🌸 Estrategia "¡Guanentino, responda!"</h1>

    <div class="text-box">
      <p>
        La estrategia <strong>"¡Guanentino, responda!"</strong> se aplica a través de un 
        <strong>sitio web interactivo y colaborativo</strong> diseñado específicamente para 
        el <strong>Colegio San José de Guanentá</strong> en San Gil, Santander. El propósito central 
        de esta implementación es <strong>incrementar el interés y la preparación académica</strong> 
        de los estudiantes para las pruebas Saber 11 / ICFES.
      </p>

      <p>
        La forma en que se viene aplicando esta estrategia se detalla a través de sus 
        funcionalidades clave y la interacción entre sus usuarios principales 
        (<em>docentes, estudiantes y administradores</em>):
      </p>

      <ul>
        <li><strong>Autenticación de Usuarios:</strong> ingreso seguro mediante PINs únicos, compatible con móviles y PC.</li>
        <li><strong>Gestión de Preguntas:</strong> docentes y administradores crean un banco colaborativo de ejercicios tipo ICFES.</li>
        <li><strong>Generación de Cuestionarios:</strong> PINs únicos por sesión, con preguntas filtradas por grado y asignatura.</li>
        <li><strong>Experiencia del Estudiante:</strong> respuestas desde dispositivos móviles con temporizador.</li>
        <li><strong>Retroalimentación Inmediata:</strong> explicaciones detalladas tras cada respuesta para fomentar el razonamiento.</li>
        <li><strong>Seguimiento del Desempeño:</strong> estadísticas y reportes exportables en PDF/Excel.</li>
        <li><strong>Gamificación:</strong> tablas de puntajes y dinámicas de juego para motivar.</li>
        <li><strong>Adaptabilidad:</strong> interfaz responsive y menú desplegable.</li>
        <li><strong>Equidad e Inclusión:</strong> tiempo extra para estudiantes con necesidades especiales.</li>
      </ul>

      <p>
        En el aspecto técnico, la estrategia se sustenta en <strong>HTML, CSS, PHP y MySQL</strong>, 
        bajo el patrón <strong>MVC</strong> y alojada en <em>Infinity Free</em>.  
      </p>
   
      <p>
        <strong>En síntesis:</strong> "¡Guanentino, responda!" es una plataforma educativa integral que 
        apoya a docentes y estudiantes, priorizando la comprensión, retroalimentación y motivación 
        en la preparación para Saber 11 / ICFES.
      </p>
