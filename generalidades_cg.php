    <h1 class="page-title">🌸 Generalidades del Colegio San José de Guanentá</h1>

    <div class="text-box">
      <p>
        El <strong>Colegio San José de Guanentá</strong> es una institución educativa ubicada en 
        <strong>San Gil, Santander</strong>. La información disponible sobre este colegio se centra 
        principalmente en el contexto de un proyecto denominado <strong>"¡Guanentino, responda!"</strong>, 
        desarrollado por estudiantes de la <strong>Especialidad de Sistemas</strong> de la misma institución.
      </p>

      <p>
        Las <strong>generalidades</strong> del Colegio San José de Guanentá, según los documentos, se relacionan 
        con los desafíos educativos que enfrentan sus estudiantes y la institución:
      </p>

      <ul>
        <li><strong>Desafío en la preparación para el ICFES:</strong> Los estudiantes enfrentan dificultades al prepararse para la prueba, debido a la tendencia a memorizar sin comprender.</li>
        <li><strong>Percepción del ICFES:</strong> Para muchos es más una carga que una oportunidad, lo que disminuye su interés y motivación.</li>
        <li><strong>Gestión del tiempo:</strong> La carga de tareas y actividades extracurriculares limita el estudio adecuado.</li>
        <li><strong>Limitaciones docentes:</strong> La falta de recursos y capacitación restringe la innovación pedagógica.</li>
        <li><strong>Impacto en resultados:</strong> Un mal desempeño reduce oportunidades de becas, ingreso a universidades y afecta la reputación institucional.</li>
        <li><strong>Brecha educativa:</strong> Puede aumentar la desigualdad entre estudiantes interesados y desmotivados, afectando el progreso social.</li>
      </ul>

      <p>
        Para abordar estas problemáticas, el proyecto <strong>"¡Guanentino, responda!"</strong> tiene como objetivo 
        <strong>desarrollar un sitio web interactivo</strong> que incremente el interés y la preparación académica de 
        los estudiantes frente a las pruebas Saber 11 / ICFES. Esta plataforma busca promover el aprendizaje 
        significativo, el pensamiento crítico y enriquecer el contenido mediante un banco colaborativo de preguntas, 
        <strong>elevando así el nivel educativo general del colegio</strong>.
      </p>
